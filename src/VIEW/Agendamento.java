    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        Agendamento objagendamento = new Agendamento();
        String dataHora_chegada = TxtChegada.getText();
        String dataHora_retirada = TxtSaida.getText();
        String trabalhador = TxtTrabalhador.getText();
        String cliente = TxtCliente.getText();
        
        objagendamento.setDataHora_chegada(dataHora_chegada);
        objagendamento.setDataHora_retirada(dataHora_retirada);
        objagendamento.setTrabalhador(trabalhador);
        objagendamento.setCliente(cliente);
        
        AgendamentoDAO objagendamentodao = new AgendamentoDAO();
        objagendamentodao.AgendarAtendimento(objagendamento);
        
        if (dataHora_chegada.isEmpty() || dataHora_retirada.isEmpty() || trabalhador.isEmpty() || cliente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ocorreu um problema ao fazer o agendamento.");
        }
          else {
            Agendamento objagendamentoview = new Agendamento();
            objagendamentoview.setVisible(true);

            this.dispose(); 
            }
    }//GEN-LAST:event_btnConfirmarActionPerformed

