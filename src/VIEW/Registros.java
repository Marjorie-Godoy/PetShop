    private void listarValoresAgendamento() {
        try {
            RegistrosDAO objregistrosdao = new RegistrosDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaRegistros.getModel();
            model.setNumRows(0);

            ArrayList<DTO.Agendamento> lista = objregistrosdao.PesquisarRegistros();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getDataHora_chegada(),
                    lista.get(num).getTrabalhador(),
                    lista.get(num).getPet(),
                    lista.get(num).getCliente(),
                    lista.get(num).getServico()
                });

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW: " + erro);
        }
    }
